
public class Combustivel {
	
	boolean gorjeta;
	double debito;

	
	public void limparVidro(double valor) {
		if (gorjeta = true) {
		this.debito = this.debito + valor * 2.00;
			
		}else {
			valor = 0;
		}
		
	}
	
	public void abastecerEtanol(double valor) {
		this.debito = this.debito + valor * 4.2;
		
	}
	
	public void abastecerGasolina(double valor) {
		this.debito = this.debito + valor * 5.5;
		
		
	}
	
	public void abastecerDiesel(double valor) {
		this.debito = this.debito + valor * 3.9;
		
		
	}
	
	


}
